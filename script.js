function fitNameFontSize(ctx, name, maxWidth) { let s = 86; while (s > 44) { ctx.font = `900 ${s}px Cairo`; if (ctx.measureText(name).width <= maxWidth) return s; s -= 4; } return s; }
function drawStudentName(ctx, name) { const centerX = 1988, baselineY = 872, maxW = 720; ctx.save(); ctx.direction = 'rtl'; ctx.textAlign = 'center'; ctx.textBaseline = 'alphabetic'; ctx.fillStyle = '#2f5689'; ctx.font = `900 ${fitNameFontSize(ctx, name, maxW)}px Cairo`; ctx.fillText(name, centerX, baselineY, maxW); ctx.restore(); }
downloadCertBtn.addEventListener('click', async () => {
  if (!window.jspdf) { alert('مكتبة PDF لم تحمل'); return; }
  const name = studentFullname.value.trim();
  if (!name) { alert('أدخل اسمك'); return; }
  downloadCertBtn.disabled = true; downloadCertBtn.textContent = 'جاري...';
  try {
    await document.fonts.ready;
    const src = typeof certificateTemplateDataUrl === 'string' ? certificateTemplateDataUrl : 'شهادة تقدير رمادي و أزرق.png';
    const img = await new Promise((res, rej) => { const i = new Image(); i.onload = () => res(i); i.onerror = rej; i.src = encodeURI(src); });
    const canvas = document.createElement('canvas'); canvas.width = img.width; canvas.height = img.height;
    const ctx = canvas.getContext('2d'); ctx.drawImage(img, 0, 0);
    drawStudentName(ctx, name);
    const pdf = new window.jspdf.jsPDF('landscape', 'mm', 'a4');
    pdf.addImage(canvas.toDataURL('image/png'), 'PNG', 0, 0, 297, 210);
    pdf.save(`certificate-${name}.pdf`);
  } catch (e) { alert('خطأ في الشهادة'); }
  finally { downloadCertBtn.disabled = false; downloadCertBtn.textContent = 'تحميل PDF'; }
});
